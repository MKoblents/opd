ORG 0x10
RES:            WORD 0
SUMM:           WORD 0
LIST_ADDR:      WORD 0x200

START:
    LD #0x10
    ST ARG1
    LD #0x11
    ST ARG2
    LD LIST_ADDR
    ST ARG3

    CALL PROCESS_LIST

    CALL CLEAR_DISPLAY
    LD RES
    ST D_TEMP_DISPLAY
    CALL OUT_DECIMAL_VU7

    CALL CLEAR_DISPLAY
    LD SUMM
    ST D_TEMP_DISPLAY
    CALL OUT_DECIMAL_VU7

    CALL CLEAR_DISPLAY
    LD VISITED_COUNT
    ST D_TEMP_DISPLAY
    CALL OUT_DECIMAL_VU7

    HLT


ORG 0x30

PROCESS_LIST:
    LD ARG3
    ST CURR_NODE
    LD ARG2
    ST PTR_SUM
    LD ARG1
    ST PTR_RES

    CLA
    ST (PTR_SUM)
    ST VISITED_COUNT

TRAVERSE:
    LD CURR_NODE
    BEQ NOT_CYCLIC

    CALL CHECK_VISITED
    LD FOUND_FLAG
    BNE CYCLE_FOUND

    CALL ADD_TO_VISITED

    LD (PTR_SUM)
    ADD (CURR_NODE)
    ST (PTR_SUM)

    LD CURR_NODE
    ADD #1
    ST L_TEMP_PTR
    LD (L_TEMP_PTR)
    ST CURR_NODE
    JUMP TRAVERSE

CYCLE_FOUND:
    LD #1
    ST (PTR_RES)
    RET

NOT_CYCLIC:
    LD #2
    ST (PTR_RES)
    RET

CHECK_VISITED:
    CLA
    ST FOUND_FLAG
    ST SEARCH_IDX
    LD VISITED_COUNT
    BEQ CHECK_END

CHECK_LOOP:
    LD SEARCH_IDX
    SUB VISITED_COUNT
    BCS CHECK_END

    LD VISITED_TABLE
    ADD SEARCH_IDX
    ST L_TEMP_PTR
    LD (L_TEMP_PTR)
    SUB CURR_NODE
    BEQ SET_FOUND

    LD SEARCH_IDX
    INC
    ST SEARCH_IDX
    JUMP CHECK_LOOP  ;05f

SET_FOUND:
    LD #1
    ST FOUND_FLAG

CHECK_END:
    RET

ADD_TO_VISITED:
    LD VISITED_TABLE
    ADD VISITED_COUNT
    ST L_TEMP_PTR

    LD CURR_NODE
    ST (L_TEMP_PTR)

    LD VISITED_COUNT
    INC
    ST VISITED_COUNT
    RET


PTR_SUM:        WORD 0
PTR_RES:        WORD 0
CURR_NODE:      WORD 0
VISITED_COUNT:  WORD 0      ;06f
VISITED_TABLE:  WORD 0x300
L_TEMP_PTR:     WORD 0
SEARCH_IDX:     WORD 0
FOUND_FLAG:     WORD 0
ARG1:           WORD 0
ARG2:           WORD 0
ARG3:           WORD 0



ORG 0x80
D_TEMP_PTR:     WORD 0
D_DIG_BUF:      WORD 0, 0, 0, 0, 0
D_TEMP_DISPLAY: WORD ?
D_TEMP_NUM:     WORD ?
D_TEMP_DIV:     WORD ?
D_TEMP_DIG:     WORD ?
D_DIGIT_CNT:    WORD ?
D_POS:          WORD ?


CLEAR_DISPLAY:
    ; LD #0x70
    ; CALL OUT_DIGIT_VU7
    ; LD #0x60
    ; CALL OUT_DIGIT_VU7
    ; LD #0x50
    ; CALL OUT_DIGIT_VU7
    LD #0x40
    CALL OUT_DIGIT_VU7
    LD #0x30
    CALL OUT_DIGIT_VU7
    LD #0x20
    CALL OUT_DIGIT_VU7
    LD #0x10
    CALL OUT_DIGIT_VU7
    LD #0x00
    CALL OUT_DIGIT_VU7
    RET

OUT_DECIMAL_VU7:
    LD D_TEMP_DISPLAY
    ST D_TEMP_NUM
    CLA
    ST D_DIGIT_CNT

    LD D_TEMP_NUM
    BNE EXTRACT_LOOP

    ; Обработка нуля
    LD #0x71
    ST D_TEMP_PTR
    LD #0
    ST (D_TEMP_PTR)
    LD #1
    ST D_DIGIT_CNT
    JUMP PRINT_LOOP

EXTRACT_LOOP:
    LD D_TEMP_NUM
    BEQ PRINT_LOOP

    CLA
    ST D_TEMP_DIV

DIV_BY_10:
    LD D_TEMP_NUM
    CMP #10
    BCC DIV_DONE
    LD D_TEMP_NUM
    SUB #10
    ST D_TEMP_NUM
    LD D_TEMP_DIV
    INC
    ST D_TEMP_DIV
    JUMP DIV_BY_10

DIV_DONE:
    LD #0x71
    ADD D_DIGIT_CNT
    ST D_TEMP_PTR
    LD D_TEMP_NUM
    ST (D_TEMP_PTR)

    LD D_TEMP_DIV
    ST D_TEMP_NUM

    LD D_DIGIT_CNT
    INC
    ST D_DIGIT_CNT
    JUMP EXTRACT_LOOP

PRINT_LOOP:
    LD D_DIGIT_CNT
    BEQ DONE_CONVERT

    DEC
    ST D_DIGIT_CNT

    LD #0x71
    ADD D_DIGIT_CNT
    ST D_TEMP_PTR
    LD (D_TEMP_PTR)
    ST D_TEMP_DIG

    LD #0
    ADD D_DIGIT_CNT
    ST D_POS

    LD D_POS
    ASL
    ASL
    ASL
    ASL
    OR D_TEMP_DIG
    CALL OUT_DIGIT_VU7

    JUMP PRINT_LOOP

DONE_CONVERT:
    RET

OUT_DIGIT_VU7:
    ST D_TEMP_DIG
WAIT_VU7:
    IN 0x15
    AND #0x40
    BEQ WAIT_VU7
    LD D_TEMP_DIG
    OUT 0x14
    RET

