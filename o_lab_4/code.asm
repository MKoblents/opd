
ORG 0x100 ; Перейти на более высокий адрес памяти для данных
LIST_START: 
    WORD 5, 0x102      ; Узел 1: Значение=5, След=0x102
NODE_2: 
    WORD 10, 0x104     ; Узел 2: Значение=10, След=0x104
NODE_3: 
    WORD 15, 0x100     ; Узел 3: Значение=15, След=0x100 (Цикл обратно на начало)
    
ORG 0x10
RES: WORD 2
SUMM: WORD 0
FIRST_ADR: WORD $LIST_START
CURR_ADR: WORD 0
START:
LD FIRST_ADR
ST CURR_ADR
ENTER:
PUSH
CALL CHECK_LOOP
POP
LOOP_START:
LD (CURR_ADR)+
ADD SUMM
ST SUMM
LD (CURR_ADR)
ST CURR_ADR
JUMP ENTER
CHECK_LOOP: BEQ SOME_LABLE
LD NUMB_EL
ST NUMB_EL2
LD ARR_END
ST CURR_ELL
LD &1
CMP -(CURR_ELL)
BNE ANOWER_LABLE
LD RES
DEC
ST RES
SOME_LABLE:
HLT
ANOWER_LABLE:
LOOP NUMB_EL2
JUMP 0x25
ST ARR_END
LD NUMB_EL
INC
ST NUMB_EL
RET
ARR_END: WORD 0x36
NUMB_EL: WORD 0
CURR_ELL: WORD 0
NUMB_EL2: WORD 0











